				<h4> Celsius to Fahrenheit:</h4>
				<p>  (&deg;Celsius x 9/5) + 32 =  &deg;Fahrenheit</p>
				<h4> Celsius to Kelvin</h4>
				<p>  &deg;Celsius +273.15 = Kelvins</p>
				<h4> Fahrenheit to Celsius:</h4>
				<p> (&deg;Fahrenheit - 32) x 5/9 = &deg;Celsius </p> 
				<h4> Fahrenheit to Kelvin</h4>
				<p> (&deg;Fahrenheit − 32) × 5/9 + 273.15 = Kelvins </p>
				<h4> Kelvin to Celsius</h4>
				<p> Kelvins − 273.15 = &deg;Celsius</p>
				<h4> Kelvin to Fahrenheit</h4>
				<p> (Kelvins− 273.15) × 9/5 + 32 =&deg;Fahrenheit</p>
