				<h2>Some History</h2>
				<h4> Fahrenheit </h4>
				<p>Fahrenheit developed the first mercury thermometer improving on an alcohol one created by Olaus Roemer, whose scale went from 0-60, based on the freezing and boiling points of water. Fahrenheit expanded the scale by multiplying by four because mercury gave him more accuracy.</p>
				<a href="https://www.livescience.com/39916-fahrenheit.html">www.livescience.com/39916-fahrenheit.html</a>
				<h4> Celsius </h4>
				<p>Celsius (an astronomer) originally had 0 as the boiling point and 100 as the freezing point. It was inverted after his death.</p>
				<a href="https://www.livescience.com/39959-celsius.html">www.livescience.com/39959-celsius.html</a>
				<h4> Kelvin </h4>
				<p>The zero point is set at the point where the volume of a gas would become zero due to moledules being unable to move. This "absolute zero" point means there are no negative values on the Kelvin scale. Also, the intervals are called "Kelvins", not degrees.</p>
